savefig("graphics/evans60/P60.eps")

#Pressure gradient
clf()
B = loadtxt("data/evans60/dP60.dat")
rc('xtick',labelsize=17)    
rc('ytick',labelsize=17) 
grid(True)
xlim(0.85,1.)
ylim(-200.,0.)
plot(B[:,0],1.e-3*B[:,1],'k',label=r'$\frac{dp_{e}}{da_{N}}$',lw=2.5)
plot(B[:,0],1.e-3*B[:,2],'k--',label=r'$\frac{dp_{i}}{da_{N}}$',lw=2.5)
plot(B[:,0],1.e-3*B[:,3],'k:',label=r'$\frac{dp_{total}}{da_{N}}$',lw=2.5)
xlabel(r'$a_{N}$',fontsize=20)
ylabel('Pressure gradients, [kPa]',fontsize=20)
legend(loc='lower left')
savefig("graphics/evans60/dP60.eps")

