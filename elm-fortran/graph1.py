from pylab import *

#Evans60
#Density
clf()
B = loadtxt("data/evans60/ne60.dat")
rc('xtick',labelsize=17)    
rc('ytick',labelsize=17) 
grid(True)
xlim(0.0,1.)
plot(B[:,0],B[:,1],'k',label=r'$n_{e}$',lw=2.5)
#plot(B[:,0],B[:,2],'k--',label='Im')
xlabel(r'$a_{N}$',fontsize=20)
ylabel(r'$n_{e}$,[$10^{19}m^{-3}$]',fontsize=20)
legend()
savefig("graphics/evans60/ne60.eps")

